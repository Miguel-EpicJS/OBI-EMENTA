#include <iostream>

using namespace std;

int main()
{

    int n1, n2; 

    n1 = 100; 
    n2 = 75;

    cout << "n1 / n2 = "  << n1 / n2 << "\n";
    cout << "n2 / n1 = "  << n2 / n1 << "\n";
    cout << "n1 / n2 = "  << (float) n1 / n2 << "\n";
    cout << "n1 / n2 = "  << n1 / 75.0 << "\n";
    cout << "n2 / n1 = "  << 75.f / n1 << "\n";


    return 0;
}
